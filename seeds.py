    response = requests.get(image_url)
    image_name = f"seed_{slug}_{i}.jpg"

    # إنشاء المقال
    post = Post(
        title=title,
        slug=slug,
        content=content,
        author=author,
        created_at=created_at,
        updated_at=created_at,
        is_published=True
    )

    if response.status_code == 200:
        post.image.save(image_name, ContentFile(response.content), save=False)

    post.save()

